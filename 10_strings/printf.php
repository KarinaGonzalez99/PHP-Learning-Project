<?php
// printf é uma função que permite formatar uma string

// %s = string
$nome = "João";
printf("O nome é %s <br>", $nome);

// %d = inteiro
$idade = 20;
printf("A idade é %d <br>", $idade);

// %f = float
$peso = 70.5;
printf("O peso é %f <br>", $peso);

// %b = boolean
$casado = false;
printf("O casado é %b <br>", $casado);
