-- Tipos de dados

VARCHAR: TEXTO DE 0 A 65535 CARACTERES
EXEMPLO:
CREATE TABLE exemplo (
    nome VARCHAR(255)
); 


CHAR: TEXTO DE 0 A 255 CARACTERES
EXEMPLO:
CREATE TABLE exemplo (
    nome CHAR(255)
);


TEXT: TEXTO DE MÁXIMO DE 65535 CARACTERES
EXEMPLO:
CREATE TABLE exemplo (
    nome TEXT
);


INT: NÚMEROS INTEIROS
EXEMPLO:
CREATE TABLE exemplo (
    idade INT
);


BIGINT: NÚMERO INTEIRO COM MAIOR PROPORÇÃO QUE O INT
EXEMPLO:
CREATE TABLE exemplo (
    idade BIGINT
);


DATE: DATA NO FORMATO 'AAAA-MM-DD'
EXEMPLO:
CREATE TABLE exemplo (
    data_nascimento DATE
);